  function drawSpriteData(data, x, y, palette, flip) {
    const pal = PALETTES[palette] || PALETTES.ra;
    const rows = data.length;
    for (let r = 0; r < rows; r++) {
      const row = data[r];
      const cols = row.length;
      for (let c = 0; c < cols; c++) {
        const idx = parseInt(row[flip ? cols - 1 - c : c]);
        if (idx > 0) {
          G.cx.fillStyle = pal[idx];
          G.cx.fillRect(Math.round(x) + c, Math.round(y) + r, 1, 1);
        }
      }
    }
  }

  function drawMapSprite(name, x, y, frame) {
    const data = SPRITE_DATA[name];
    if (!data) return;
    // Simple walk animation: shift pixels slightly based on frame
    drawSpriteData(data, x, y, name.split('_')[0]);
  }

