// ─── Main Seed Function ──────────────────────────────────

async function main() {
  console.log("Seeding 20 enterprise AI systems with full profiles...\n");

  for (const sys of systems) {
    const { industrySlugs, scores, ...systemData } = sys;

    // Upsert the system
    const created = await prisma.aISystem.upsert({
      where: { slug: sys.slug },
      update: {
        ...systemData,
        industries: { set: industrySlugs.map((slug) => ({ slug })) },
      },
      create: {
        ...systemData,
        industries: { connect: industrySlugs.map((slug) => ({ slug })) },
      },
    });

    // Upsert scores
    for (const [frameworkSlug, score] of Object.entries(scores)) {
      const framework = await prisma.regulatoryFramework.findUnique({
        where: { slug: frameworkSlug },
      });
      if (framework) {
        await prisma.assessmentScore.upsert({
          where: { systemId_frameworkId: { systemId: created.id, frameworkId: framework.id } },
          update: { score },
          create: { score, systemId: created.id, frameworkId: framework.id },
        });
      }
    }

    console.log(`  ✓ ${sys.vendor} ${sys.name}`);
  }

  console.log(`\nDone! Seeded ${systems.length} systems.`);
}

main()
  .catch((e) => { console.error("Seed failed:", e); process.exit(1); })
  .finally(() => prisma.$disconnect());
